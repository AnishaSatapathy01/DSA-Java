public class Strings {
    public static void main (String args[]){
        String name ="Anisha";
        System.out.println(name);
        // llength of string
        System.out.println(name.length());
        //charAt
        System.out.println(name.charAt(2));
        for (int i =0; i<name.length();i++){
            System.out.println(name.charAt(i));
        }


    }
}
